package gr.charos.homeapp.contractions.model;

import java.util.Date;

public class Contraction {
	Date start;
	Date finish;
	
	public Date getStart() {
		return start;
	}
	public void setStart(Date start) {
		this.start = start;
	}
	public Date getFinish() {
		return finish;
	}
	public void setFinish(Date finish) {
		this.finish = finish;
	}
	

}
